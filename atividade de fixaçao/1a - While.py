#Programa que apresenta os quadrados dos números 15 a 200 usando o while

n=15 
while n<=200:
    print("O quadrado de",n, "é", n**2)
    n+=1             

#Explicação do código: 
# 1) na variável "n" é atribuído o valor 15 como início dos valores que o exercício pede.
# 2) a estrutura de repetição "while" delimita os valores até 200.
# 3) "n+=1" serve para ir adicionando +1 ao valor de n até chegar na condição estabelecida pelo while.